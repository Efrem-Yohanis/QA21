// for loop

for (let i = 0; i < 5; i++) {
  console.log(i);
}

// while loop

let i = 0;

while (i < 5) {
  console.log(i);
  i++;
}

// do while loop

let j = 0;
do {
  console.log(j);
  i++;
} while (j < 5);

// break statement

for (let i = 0; i < 10; i++) {
  if (i === 5) {
    break;
  }
  console.log(i);
}

// continuse Statement
for (let i = 0; i < 5; i++) {
  if (i === 3) {
    continue;
  }
  console.log(i);
}
